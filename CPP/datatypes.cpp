#include<iostream>
using namespace std;
int main()
{
	int a=10;
	float b=20.2;
	double c=172.123;
	char d='a';
	string s="Anusha";
	bool boolean= true;
	cout << "Integer = " << a << endl;
	cout << "Float = " << b << endl;
	cout << "Double = " << c << endl;
	cout << "Char = " << d << "\n" << "String = " << s << endl;
	cout << "Boolean = " << boolean << endl; // outputs 1 if it's true else 0
	char x=65, y=66, z=67;
	cout << x << endl << y << endl << z << endl;
}
	
